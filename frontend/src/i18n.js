// ENSURE i18n INITIALIZATION BEFORE REACT RENDER
const savedLang = localStorage.getItem("lang");
const validLangs = ["en", "hi", "te"];
const defaultLang = "en";

// Validate saved language
const language = (savedLang && validLangs.includes(savedLang)) ? savedLang : defaultLang;

// If invalid language was saved, clean it up
if (savedLang && !validLangs.includes(savedLang)) {
  console.warn(`Invalid language "${savedLang}" found in localStorage. Resetting to "${defaultLang}".`);
  localStorage.setItem("lang", defaultLang);
}

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: language,
    fallbackLng: defaultLang,
    supportedLngs: validLangs,
    interpolation: {
      escapeValue: false
    },
    react: {
      useSuspense: false // Set to false to prevent blank screens if not using Suspense
    }
  });

console.log("i18n initialized. Current language:", i18n.language);

